public class Star {
    double x;
    double y;
    public Star(double xx, double yy) {
        x=xx;
        y=yy;
    }
    public double getX() {
        return x;
    }
    public double getY() {
        return y;
    }
    public void setX(double xx) {
        x=xx;
    }
    public void setY(double yy) {
        y=yy;
    }
}